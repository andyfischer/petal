//! Tiny embedded 5x7 bitmap font. Each glyph is 7 rows × 5 bits (LSB = right).
//!
//! Only covers ASCII 32..126 — that's plenty for HUD text. Missing characters
//! render as a hollow box so bugs stay visible.

pub const FONT_WIDTH: usize = 5;
pub const FONT_HEIGHT: usize = 7;

pub fn glyph(ch: char) -> [u8; 7] {
    let c = ch as u32;
    if c < 32 || c > 126 {
        return UNKNOWN;
    }
    FONT[(c - 32) as usize]
}

